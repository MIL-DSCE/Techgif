package org.telegram.ui.Components;

public class Rect {
    public float height;
    public float width;
    public float f16x;
    public float f17y;

    public Rect(float x, float y, float width, float height) {
        this.f16x = x;
        this.f17y = y;
        this.width = width;
        this.height = height;
    }
}
